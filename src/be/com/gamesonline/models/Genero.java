package be.com.gamesonline.models;

public class Genero {
	
	
	private int gen_id;
	
	private String nome_genero;
	
	private String plataforma;

	
	public int getGen_id() {
		return gen_id;
	}

	public void setGen_id(int gen_id) {
		this.gen_id = gen_id;
	}


	public String getNome_genero() {
		return nome_genero;
	}

	public void setNome_genero(String nome_genero) {
		this.nome_genero = nome_genero;
	}

	public String getPlataforma() {
		return plataforma;
	}

	public void setPlataforma(String plataforma) {
		this.plataforma = plataforma;
	}
	
	

	

	
	
	

	
}
